export class Post {
    constructor(image, caption, type, source_type, source_link, date, likes, name, profile_image) {
        this.image = image
        this.caption = caption
        this.type = type
        this.source_type = source_type
        this.source_link = source_link
        this.date = date
        this.likes = likes
        this.name = name
        this.profile_image = profile_image
    }
}